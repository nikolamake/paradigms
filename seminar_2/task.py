# ● Условие На вход подается число n.
# ● Задача Написать скрипт в любой парадигме, который выводит на экран таблицу умножения всех чисел от 1 до n.
# Обоснуйте выбор парадигм. ● Пример вывода: 1 * 1 = 1
# 1 * 2 = 2
# 1 * 3 = 3
# 1 * 4 = 4
# 1 * 5 = 5
# 1 * 6 = 6
# ..
# 1 * 9 = 9

# Императивное программирование подходит для этой задачи,
# потому что оно позволяет явно задавать последовательность действий,
# которые нужно выполнить для получения желаемого результата.

# Получаем число n от пользователя
n = int(input("Введите число n: "))

# Создаем два цикла for для перебора всех чисел от 1 до n
for i in range(1, n + 1):
    for j in range(1, n + 1):
        # Выводим произведение i и j на экран
        print(f"{i} * {j} = {i * j}")
    # Переходим на новую строку после каждого ряда
    print()